// `autoresearch container-snapshot --id <run-id>` — write a Dockerfile +
// requirements snapshot that would (best-effort) recreate the run's environment.
//
// We don't ship a Dockerfile that's guaranteed to bit-reproduce the run —
// that's a much bigger project. We DO ship one that gets a reviewer 95% of
// the way there: same Python minor version, same Torch + CUDA selector, plus
// the exact `pip freeze`-able dependency list captured at run time (when
// available via env.pip_freeze).
//
// Output goes to `.researchloop/scratchpad/runs/<id>/repro/` by default.

import fs from "node:fs";
import path from "node:path";
import { readLedgerRows, findRowById, ensureDir } from "./researchloop-core.js";

function pickPythonImage(env) {
  // Strict: only Python image, no CUDA. CUDA-bearing base images depend on
  // the cluster's NVIDIA driver and are too easy to mis-spec; we leave the
  // CUDA decision to the reviewer with a clear comment.
  const py = String(env.python || "").match(/(\d+\.\d+)/);
  const minor = py ? py[1] : "3.11";
  return `python:${minor}-slim`;
}

function pickCudaBase(env) {
  const cuda = String(env.cuda || "").match(/(\d+)\.(\d+)/);
  if (!cuda) return null;
  const tag = `${cuda[1]}.${cuda[2]}.0-cudnn8-runtime-ubuntu22.04`;
  return `nvidia/cuda:${tag}`;
}

function buildDockerfile(row, opts) {
  const env = row.env || {};
  const cudaBase = pickCudaBase(env);
  const baseImage = opts.gpu && cudaBase ? cudaBase : pickPythonImage(env);
  const lines = [];
  lines.push(`# Best-effort reproduction Dockerfile for run: ${row.id}`);
  lines.push(`# Generated by \`autoresearch container-snapshot\` on ${new Date().toISOString()}`);
  lines.push(`#`);
  lines.push(`# Captured env:`);
  lines.push(`#   python: ${env.python || "?"}`);
  lines.push(`#   torch:  ${env.torch || "?"}`);
  lines.push(`#   cuda:   ${env.cuda || "?"}`);
  lines.push(`#   gpu:    ${Array.isArray(env.gpu) ? env.gpu.join(", ") : (env.gpu || "?")}`);
  lines.push(`#   git_sha: ${env.git_sha || "?"} (dirty=${env.git_dirty === true ? "yes" : "no"})`);
  if (!opts.gpu) {
    lines.push(`#`);
    lines.push(`# This Dockerfile uses a CPU Python base. For GPU reproduction, re-run with --gpu`);
    lines.push(`# (and confirm the cluster's NVIDIA driver supports the CUDA tag above).`);
  } else if (!cudaBase) {
    lines.push(`#`);
    lines.push(`# WARNING: --gpu requested but env.cuda is missing — falling back to Python base.`);
  }
  lines.push("");
  lines.push(`FROM ${baseImage}`);
  lines.push("");
  if (opts.gpu) {
    lines.push("# Install Python on top of the CUDA image.");
    lines.push("RUN apt-get update && apt-get install -y --no-install-recommends \\");
    lines.push("    python3 python3-pip python3-venv git && \\");
    lines.push("    rm -rf /var/lib/apt/lists/*");
    lines.push("RUN ln -sf /usr/bin/python3 /usr/bin/python");
    lines.push("");
  }
  lines.push("WORKDIR /workspace");
  lines.push("");
  if (env.pip_freeze && Array.isArray(env.pip_freeze) && env.pip_freeze.length > 0) {
    lines.push("# Pinned dependencies captured at run time.");
    lines.push("COPY requirements.lock /workspace/requirements.lock");
    lines.push("RUN pip install --no-cache-dir -r /workspace/requirements.lock");
  } else {
    lines.push("# No pip_freeze captured for this run — copy your requirements.txt manually.");
    lines.push("COPY requirements.txt /workspace/requirements.txt");
    lines.push("RUN pip install --no-cache-dir -r /workspace/requirements.txt");
  }
  lines.push("");
  if (env.git_sha) {
    lines.push(`# Code: git checkout the recorded SHA after \`COPY . /workspace\` or build inside the worktree.`);
    lines.push(`#   git fetch && git checkout ${env.git_sha}`);
    lines.push("");
  }
  lines.push(`# Reproduce the run:`);
  lines.push(`#   ${row.command || "(command not recorded)"}`);
  lines.push("");
  return lines.join("\n");
}

function buildRequirementsLock(row) {
  const env = row.env || {};
  if (Array.isArray(env.pip_freeze) && env.pip_freeze.length > 0) {
    return env.pip_freeze.join("\n") + "\n";
  }
  return null;
}

function buildReadme(row, opts) {
  const env = row.env || {};
  return [
    `# Reproduce run: ${row.id}`,
    ``,
    `Generated by \`autoresearch container-snapshot\` on ${new Date().toISOString()}.`,
    ``,
    `## What this is`,
    ``,
    `A best-effort container snapshot that gets you within reach of bit-equivalent`,
    `reproduction. It pins Python minor version, the dependency list (if captured),`,
    `and points at the exact git SHA the run used. It does NOT guarantee numerical`,
    `equivalence on a different GPU — for that, also pass through autoresearch's`,
    `determinism audit ([[autoresearch-determinism]]) after building.`,
    ``,
    `## Files`,
    ``,
    `- \`Dockerfile\`         — ${opts.gpu ? "CUDA-based" : "CPU Python"} base + dependency install`,
    `- \`requirements.lock\`  — captured \`pip freeze\` from env.pip_freeze${Array.isArray(env.pip_freeze) && env.pip_freeze.length ? "" : " (placeholder; not captured)"}`,
    `- \`README.md\`          — this file`,
    ``,
    `## Build & run`,
    ``,
    `\`\`\`bash`,
    `docker build -t reproduce-${row.id} .`,
    `# Mount the project repo (with code at git SHA ${env.git_sha || "?"}):`,
    `docker run --rm -it -v "$PWD:/workspace" reproduce-${row.id} \\`,
    `  bash -c "${(row.command || "echo no-command").replace(/"/g, '\\"')}"`,
    `\`\`\``,
    ``,
    `## Caveats`,
    ``,
    `- env.git_dirty was \`${env.git_dirty === true ? "true" : "false"}\` — if true, uncommitted changes are NOT captured here`,
    `- env.gpu was \`${Array.isArray(env.gpu) ? env.gpu.join(", ") : (env.gpu || "?")}\` — different hardware can produce different metrics`,
    `- env.cuda was \`${env.cuda || "?"}\` — the cudnn algorithm picker can differ across versions`,
    ``,
  ].join("\n");
}

export async function cmdContainerSnapshot(ctx) {
  const { option, hasFlag, targetDir } = ctx;
  const cwd = targetDir();

  const runId = String(option("--id", "")).trim();
  if (!runId) {
    console.error("Usage: autoresearch container-snapshot --id <run-id> [--gpu] [--out DIR] [--format text|json] [--dir PATH]");
    process.exitCode = 1;
    return;
  }
  const rows = readLedgerRows(cwd);
  const row = findRowById(rows, runId);
  if (!row) { console.error(`Run not found: ${runId}`); process.exitCode = 1; return; }
  if (!row.env) {
    console.error(`Run ${runId} has no env capture — container snapshot would be a guess. Re-run after G14 env capture is enabled.`);
    process.exitCode = 1;
    return;
  }

  const opts = { gpu: hasFlag("--gpu") };
  const dockerfile = buildDockerfile(row, opts);
  const requirements = buildRequirementsLock(row);
  const readme = buildReadme(row, opts);

  const outDir = option("--out", null) || path.join(cwd, ".researchloop", "scratchpad", "runs", runId, "repro");
  ensureDir(outDir);
  fs.writeFileSync(path.join(outDir, "Dockerfile"), dockerfile);
  fs.writeFileSync(path.join(outDir, "README.md"), readme);
  if (requirements) {
    fs.writeFileSync(path.join(outDir, "requirements.lock"), requirements);
  } else {
    fs.writeFileSync(path.join(outDir, "requirements.lock"), "# env.pip_freeze was not captured for this run.\n# Replace with your project's requirements.\n");
  }

  console.log(`wrote container snapshot for ${runId} -> ${path.relative(cwd, outDir)}`);
  console.log("  - Dockerfile");
  console.log("  - requirements.lock" + (requirements ? "" : "  (placeholder)"));
  console.log("  - README.md");
  console.log(`next: cd ${path.relative(cwd, outDir)} && docker build -t reproduce-${runId} .`);
}
