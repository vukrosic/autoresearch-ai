// `autoresearch share <run-id>` — package one run as a portable bundle.
//
// Output is a single `.tar.gz` containing:
//   - the full run directory (`.researchloop/scratchpad/runs/<id>/`)
//   - the ledger row as `row.json`
//   - a `MANIFEST.txt` listing every file with sha256
//   - a `README.md` with reproduction instructions
//
// No upload, no network. The caller can attach the file to a PR, ship it via
// scp, or hand it to a collaborator. The receiver can inspect it with
// standard tools (`tar`, `sha256sum`) without installing autoresearch.

import fs from "node:fs";
import path from "node:path";
import os from "node:os";
import { createHash } from "node:crypto";
import { execSync } from "node:child_process";
import { readLedgerRows, findRowById, fmt } from "./researchloop-core.js";

function sha256OfFile(file) {
  const h = createHash("sha256");
  h.update(fs.readFileSync(file));
  return h.digest("hex");
}

function walk(dir) {
  const out = [];
  function rec(d) {
    if (!fs.existsSync(d)) return;
    for (const entry of fs.readdirSync(d)) {
      const full = path.join(d, entry);
      const st = fs.statSync(full);
      if (st.isDirectory()) rec(full);
      else out.push(full);
    }
  }
  rec(dir);
  return out;
}

export async function cmdShare(ctx) {
  const { option, targetDir, args } = ctx;
  const cwd = targetDir();

  const idx = args.findIndex((a) => a === "share");
  let runId = String(option("--id", "")).trim();
  if (!runId && idx !== -1 && args[idx + 1] && !args[idx + 1].startsWith("-")) {
    runId = String(args[idx + 1]).trim();
  }
  if (!runId) {
    console.error("Usage: autoresearch share <run-id> [--out FILE.tar.gz] [--dir PATH]");
    process.exitCode = 1;
    return;
  }

  const rows = readLedgerRows(cwd);
  const row = findRowById(rows, runId);
  if (!row) {
    console.error(`Run not found: ${runId}`);
    process.exitCode = 1;
    return;
  }

  const runDir = path.join(cwd, ".researchloop", "scratchpad", "runs", runId);
  if (!fs.existsSync(runDir)) {
    console.error(`No run directory at ${runDir}.`);
    process.exitCode = 1;
    return;
  }

  // Stage in a tmpdir so we don't pollute the project.
  const stamp = new Date().toISOString().replace(/[:.]/g, "-");
  const stagingRoot = fs.mkdtempSync(path.join(os.tmpdir(), `autoresearch-share-${runId}-`));
  const stagingRun = path.join(stagingRoot, runId);
  fs.mkdirSync(stagingRun, { recursive: true });

  // Copy run dir.
  const runFiles = walk(runDir);
  for (const src of runFiles) {
    const rel = path.relative(runDir, src);
    const dest = path.join(stagingRun, rel);
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(src, dest);
  }

  // Write row.json.
  fs.writeFileSync(path.join(stagingRun, "row.json"), JSON.stringify(row, null, 2) + "\n");

  // Write MANIFEST.txt with sha256s of every file we're including.
  const manifestFiles = walk(stagingRun);
  const manifestLines = [
    `# autoresearch share manifest`,
    `# generated: ${new Date().toISOString()}`,
    `# run_id: ${runId}`,
    `# n_files: ${manifestFiles.length}`,
    ``,
  ];
  for (const f of manifestFiles) {
    const rel = path.relative(stagingRun, f);
    const st = fs.statSync(f);
    manifestLines.push(`${sha256OfFile(f)}  ${st.size}  ${rel}`);
  }
  fs.writeFileSync(path.join(stagingRun, "MANIFEST.txt"), manifestLines.join("\n") + "\n");

  // Write README.md with reproduction steps.
  const metricKeys = row.metrics ? Object.keys(row.metrics).filter((k) => !k.endsWith("_std")) : [];
  const primary = metricKeys[0] || null;
  const readme = [
    `# Run share: ${runId}`,
    ``,
    `Generated by \`autoresearch share\` on ${new Date().toISOString()}.`,
    ``,
    `## Summary`,
    ``,
    `- Status: \`${row.status || "unknown"}\``,
    primary ? `- Primary metric: \`${primary} = ${fmt(Number(row.metrics[primary]), 6)}\`` : "- Primary metric: (none recorded)",
    `- Command: \`${row.command || "(none recorded)"}\``,
    row.env ? `- Env: python=${row.env.python || "?"}, torch=${row.env.torch || "?"}, cuda=${row.env.cuda || "?"}, gpu=${Array.isArray(row.env.gpu) ? row.env.gpu.join(", ") : (row.env.gpu || "?")}` : "",
    row.env && row.env.git_sha ? `- Git SHA: \`${row.env.git_sha}\` (dirty=${row.env.git_dirty ? "yes" : "no"})` : "",
    ``,
    `## Files`,
    ``,
    `- \`log.txt\` — full stdout/stderr of the run`,
    `- \`env.json\` — captured environment`,
    `- \`code.diff\` — git diff at run start (may be empty)`,
    `- \`config.json\` — autoresearch invocation context`,
    `- \`metrics.jsonl\` — streamed metric samples`,
    `- \`row.json\` — full ledger row`,
    `- \`MANIFEST.txt\` — sha256 + size for every file in this bundle`,
    ``,
    `## Verify`,
    ``,
    `\`\`\`bash`,
    `tar -xzf ${runId}-${stamp}.tar.gz`,
    `cd ${runId}`,
    `# Recompute sha256s and diff against MANIFEST.txt`,
    `while read sha size rel; do echo "$(shasum -a 256 "$rel" | awk '{print $1}') $size $rel"; done < <(grep -v '^#' MANIFEST.txt) > recomputed.txt`,
    `diff <(grep -v '^#' MANIFEST.txt | awk '{print $1, $2, $3}') recomputed.txt`,
    `\`\`\``,
    ``,
    `## Reproduce`,
    ``,
    `From the original repo at the same git SHA:`,
    ``,
    `\`\`\`bash`,
    `autoresearch replay ${runId}`,
    `\`\`\``,
    ``,
  ].filter((l) => l !== null).join("\n");
  fs.writeFileSync(path.join(stagingRun, "README.md"), readme);

  const outRaw = option("--out", null);
  const outPath = (outRaw && typeof outRaw === "string")
    ? path.resolve(outRaw)
    : path.resolve(cwd, `${runId}-${stamp}.tar.gz`);

  // tar -czf: portable across macOS and Linux.
  try {
    execSync(`tar -czf ${JSON.stringify(outPath)} -C ${JSON.stringify(stagingRoot)} ${JSON.stringify(runId)}`);
  } catch (err) {
    console.error("tar failed: " + err.message);
    fs.rmSync(stagingRoot, { recursive: true, force: true });
    process.exitCode = 1;
    return;
  }

  // Clean staging.
  fs.rmSync(stagingRoot, { recursive: true, force: true });

  const sz = fs.statSync(outPath).size;
  console.log(`wrote ${outPath} (${(sz / 1024).toFixed(1)} KiB, ${manifestFiles.length} files)`);
}
