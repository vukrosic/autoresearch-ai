// `autoresearch slurm` — wrap any command as an sbatch script.
//
// Output is a self-contained `.sbatch` file the user can `sbatch <file>` on
// their cluster. The script:
//   - sets standard sbatch directives (partition, gres, mem, time, output)
//   - sources the user's training env (`--env-script` if given)
//   - records the slurm job id back into the autoresearch ledger via a
//     post-run hook so `runs.jsonl` rows carry `slurm_job_id`
//   - invokes `autoresearch run --command ...` so safety + env + artifacts
//     all behave the same as a local run
//
// No actual submission — that's `sbatch` itself. We just generate the script.

import fs from "node:fs";
import path from "node:path";

function defaultJobName(cmdText) {
  const t = cmdText.replace(/[^A-Za-z0-9-]+/g, "-").replace(/^-+|-+$/g, "").slice(0, 40);
  return t || "autoresearch-run";
}

function buildScript(opts) {
  const lines = [];
  lines.push("#!/usr/bin/env bash");
  lines.push("#");
  lines.push("# Generated by `autoresearch slurm`. Submit with: sbatch <this file>");
  lines.push("#");
  lines.push(`#SBATCH --job-name=${opts.jobName}`);
  lines.push(`#SBATCH --output=${opts.outputPattern}`);
  lines.push(`#SBATCH --error=${opts.errorPattern}`);
  lines.push(`#SBATCH --time=${opts.time}`);
  lines.push(`#SBATCH --partition=${opts.partition}`);
  if (opts.gpus > 0) lines.push(`#SBATCH --gres=gpu:${opts.gpus}`);
  if (opts.cpus > 0) lines.push(`#SBATCH --cpus-per-task=${opts.cpus}`);
  if (opts.mem) lines.push(`#SBATCH --mem=${opts.mem}`);
  if (opts.nodes > 1) lines.push(`#SBATCH --nodes=${opts.nodes}`);
  if (opts.account) lines.push(`#SBATCH --account=${opts.account}`);
  if (opts.constraint) lines.push(`#SBATCH --constraint=${opts.constraint}`);
  if (opts.exclusive) lines.push("#SBATCH --exclusive");
  lines.push("");
  lines.push("set -euo pipefail");
  lines.push("");
  lines.push("echo \"slurm_job_id=$SLURM_JOB_ID\"");
  lines.push("echo \"slurm_node=$SLURMD_NODENAME\"");
  lines.push("echo \"hostname=$(hostname)\"");
  lines.push("echo \"date=$(date -u +%Y-%m-%dT%H:%M:%SZ)\"");
  lines.push("");
  if (opts.envScript) {
    lines.push(`# user env`);
    lines.push(`source ${opts.envScript}`);
    lines.push("");
  }
  if (opts.modulesLoad && opts.modulesLoad.length > 0) {
    for (const m of opts.modulesLoad) lines.push(`module load ${m}`);
    lines.push("");
  }
  lines.push(`cd ${opts.cwd}`);
  lines.push("");
  // Embed the autoresearch invocation. The inner command is heredoc-safe.
  lines.push(`autoresearch run \\`);
  lines.push(`  --id ${opts.runId} \\`);
  lines.push(`  --metric ${opts.metric} \\`);
  lines.push(`  --timeout ${opts.timeoutSec} \\`);
  if (opts.allowUnsafe) lines.push(`  --allow-unsafe \\`);
  lines.push(`  --command "${opts.cmdText.replace(/"/g, '\\"')}"`);
  lines.push("");
  lines.push(`# Post-run: write slurm metadata into the ledger row.`);
  lines.push(`if command -v jq >/dev/null; then`);
  lines.push(`  ledger=".researchloop/scratchpad/runs.jsonl"`);
  lines.push(`  if [ -f "$ledger" ]; then`);
  lines.push(`    tmp=$(mktemp)`);
  lines.push(`    while IFS= read -r line; do`);
  lines.push(`      id=$(echo "$line" | jq -r '.id')`);
  lines.push(`      if [ "$id" = "${opts.runId}" ]; then`);
  lines.push(`        echo "$line" | jq --arg jid "$SLURM_JOB_ID" --arg node "$SLURMD_NODENAME" '.slurm_job_id=$jid | .slurm_node=$node'`);
  lines.push(`      else`);
  lines.push(`        echo "$line"`);
  lines.push(`      fi`);
  lines.push(`    done < "$ledger" > "$tmp"`);
  lines.push(`    mv "$tmp" "$ledger"`);
  lines.push(`  fi`);
  lines.push(`fi`);
  lines.push("");
  return lines.join("\n");
}

export async function cmdSlurm(ctx) {
  const { option, hasFlag, targetDir } = ctx;
  const cwd = targetDir();

  const cmdRaw = option("--command", null);
  const cmdText = cmdRaw && typeof cmdRaw === "string" ? cmdRaw : "";
  if (!cmdText) {
    console.error("Usage: autoresearch slurm --command CMD [--partition NAME] [--gpus N] [--cpus N] [--mem 32G] [--time 02:00:00] [--nodes N] [--account NAME] [--constraint NAME] [--env-script PATH] [--modules m1,m2] [--exclusive] [--id ID] [--metric NAME] [--timeout SECONDS] [--allow-unsafe] [--out FILE.sbatch] [--dir PATH]");
    process.exitCode = 1;
    return;
  }

  const opts = {
    cwd,
    cmdText,
    runId: String(option("--id", `slurm-${new Date().toISOString().replace(/[:.]/g, "-")}`)),
    metric: String(option("--metric", "val_loss")).trim() || "val_loss",
    timeoutSec: Number(option("--timeout", 3600)),
    allowUnsafe: hasFlag("--allow-unsafe"),
    partition: String(option("--partition", "gpu")),
    gpus: Math.max(0, parseInt(String(option("--gpus", "1")), 10) || 0),
    cpus: Math.max(0, parseInt(String(option("--cpus", "8")), 10) || 0),
    mem: option("--mem", "32G"),
    time: String(option("--time", "02:00:00")),
    nodes: Math.max(1, parseInt(String(option("--nodes", "1")), 10) || 1),
    account: option("--account", null),
    constraint: option("--constraint", null),
    exclusive: hasFlag("--exclusive"),
    envScript: option("--env-script", null),
    modulesLoad: String(option("--modules", "")).split(",").map((s) => s.trim()).filter(Boolean),
    jobName: option("--job-name", null) || defaultJobName(cmdText),
    outputPattern: option("--output", "slurm-%j.out"),
    errorPattern: option("--error", "slurm-%j.err"),
  };

  const script = buildScript(opts);
  const outRaw = option("--out", null);
  if (outRaw && typeof outRaw === "string") {
    fs.writeFileSync(outRaw, script);
    try { fs.chmodSync(outRaw, 0o755); } catch {}
    console.log(`wrote ${outRaw}`);
    console.log(`submit with: sbatch ${outRaw}`);
  } else {
    process.stdout.write(script);
  }
}
